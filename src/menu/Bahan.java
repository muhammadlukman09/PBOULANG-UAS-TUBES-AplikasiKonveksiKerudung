    private void setDataToTable(){
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("Nama Bahan");
        tabel.addColumn("Jenis Bahan");
        tabel.addColumn("Warna");

        try{           
            String sql = "SELECT * FROM bahanbaku_kain ORDER BY name ASC";
            java.sql.Connection Conn = (Connection) Koneksi.configDB();
            java.sql.Statement st = Conn.createStatement();
            java.sql.ResultSet res = st.executeQuery(sql);
            while(res.next()){
                tabel.addRow(new Object[]{
                    res.getString(2),res.getString(3),res.getString(4)
                });
                idBahanArr.add(res.getInt(1));
            }
            bahanBakuTable.setModel(tabel);
        }catch (SQLException e){
            System.out.println(e);
        }    
    }

