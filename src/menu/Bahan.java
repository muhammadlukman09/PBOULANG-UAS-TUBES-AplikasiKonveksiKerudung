    private void bahanBakuTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bahanBakuTableMouseClicked
        int baris = bahanBakuTable.rowAtPoint(evt.getPoint());
        idBahan = idBahanArr.get(baris);
        namaBahan = bahanBakuTable.getValueAt(baris, 0).toString();
    }//GEN-LAST:event_bahanBakuTableMouseClicked

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        BahanForm p= new BahanForm();
        desktop.add(p);
        p.setDeskop(desktop, w, h);
        p.setLocation((w - p.getSize().width)/2, (h-p.getSize().height)/2);
        p.setMode("ADD", 0);
        p.toFront();
        p.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_addButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        if (idBahan > 0){
            BahanForm p= new BahanForm();
            desktop.add(p);
            p.setDeskop(desktop, w, h);
            p.setLocation((w - p.getSize().width)/2, (h-p.getSize().height)/2);
            p.setMode("EDIT", idBahan);
            p.toFront();
            p.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Silahkan pilih data yang akan di edit");
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if (idBahan > 0){
            int a;
            a=JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus Data ini \nid :"+idBahan+"\nnama : "+namaBahan+" ?", "HAPUS", JOptionPane. YES_NO_OPTION);
            if(a == JOptionPane.YES_OPTION){
                try{
                    String del = "DELETE FROM bahanbaku_kain WHERE id_kain = '"+idBahan+"'";
                    java.sql.Connection Conn = (Connection) Koneksi.configDB();
                    java.sql.PreparedStatement sql = Conn.prepareStatement(del);
                    sql.execute();
                    JOptionPane.showMessageDialog(null, "Berhasil Dihapus");
                    setDataToTable();
                }catch (HeadlessException | SQLException e){
                    JOptionPane.showMessageDialog(null, "Hapus Gagal..!!");
                    System.out.println(e.getMessage());
                }
            } 
        }else{
            JOptionPane.showMessageDialog(null, "Silahkan pilih data yang akan di edit");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed


